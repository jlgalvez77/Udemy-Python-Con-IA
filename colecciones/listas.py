print('*** Listas ***')

mi_lista = [1, 2, 3, 4, 5]

# Acceder a un elemento por su indice
print(mi_lista[4])

# Modificar un elemento de la lista
mi_lista[1] = 10
print(mi_lista)

# Agregar un nuevo elemento a la lista
mi_lista.append(6)
print(mi_lista)

# Eliminar elementos de la lista
del mi_lista[2]
print(mi_lista)

# Saber la longitud de la lista
print(len(mi_lista))